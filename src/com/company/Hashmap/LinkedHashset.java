package com.company.Hashmap;

import java.util.LinkedHashSet;

public class LinkedHashset {
    public static void main( String[] args ) {
        LinkedHashSet<String> lhs = new LinkedHashSet<>();
        lhs.add("delhi");
        lhs.add("mumbai");
        lhs.add("kolkatta");
        lhs.add("jaipur");
        lhs.add("mathura");
        System.out.println(lhs);
    }
}
